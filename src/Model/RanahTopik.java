public class RanahTopik {
    String Kode;
    String Subjek;

    public RanahTopik(String Kode, String Subjek) {
        this.Kode = Kode;
        this.Subjek = Subjek;
    }

    public String getKode() {
        return Kode;
    }

    public void setKode(String Kode) {
        this.Kode = Kode;
    }

    public String getSubjek() {
        return Subjek;
    }

    public void setSubjek(String Subjek) {
        this.Subjek = Subjek;
    }
    
    
}
